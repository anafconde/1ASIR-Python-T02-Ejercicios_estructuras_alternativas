# Algoritmo que pida dos números e indique si el primero 
# es mayor que el segundo o no.

num1= int(input("dame un numero "))
num2= int(input("dame otro numero "))

if num1 > num2:
    print("El",num1, "es mayor que el ",num2)
else:
    print("El",num1, "es menor que el ",num2)