# Algoritmo que pida dos números e indique si el primero es mayor que el segundo o no.

numero = int(input("Indica un número: "))
numero1 = int(input("Indica otro número: "))

if numero > numero1: 
    print("Este número", numero, "es mayor que", numero1)
    
else:
    print("Este número", numero, "es menor que", numero1)
